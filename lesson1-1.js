const NAME = 'name';        // Объявл константу NAME
let Surname = 'surname';    // Объявил переменную Surname

console.log(NAME);          // Вывел в консоль значение константы NAME
console.log(Surname);       // Вывел в консоль значение переменной Surname

console.log(NAME, Surname); // Вывел в консоль значение константы NAME и переменной Surname

surname = 'new surname';    // Изменил значение переменной Surname

